import java.util.*;
class switchcase
{
public static void main(String args[])
{
Scanner sc=new Scanner(System.in);
int number=sc.nextInt();
switch(number)
{
case 1:
System.out.println("mon");
break;
case 2:
System.out.println("tue");
break;
case 3:
System.out.println("wed");
break;
default:
System.out.println("invalid");
break;

}
}}


